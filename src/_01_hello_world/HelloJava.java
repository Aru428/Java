package _01_hello_world;

public class HelloJava {
    // 자바는 대소문자를 구분한다!
    // public class _01_hello_world.HelloJava -> _01_hello_world.HelloJava 클래스를 의미
    // 지금은 단순히 _01_hello_world.HelloJava.java 라는 파일을 만들었다고 이해
    // 파일명과 클래스명은 같아야 한다
    public static void main(String[] args) {    // main 메서드
        // main은 프로그램의 시작점이다.
        System.out.println("Hello Java");
    }
}
